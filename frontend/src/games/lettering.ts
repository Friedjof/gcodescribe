function normalizeGameText(text: string): string {
  return text
    .replace(/ß/g, "ss")
    .replace(/Æ/g, "AE")
    .replace(/æ/g, "ae")
    .replace(/Ø/g, "O")
    .replace(/ø/g, "o")
    .replace(/Œ/g, "OE")
    .replace(/œ/g, "oe")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toUpperCase();
}

function stylizePoint([x, y]: Pt, unit: number): Pt {
  const slantedX = x * WIDTH_SCALE + y * SLANT;
  return [slantedX * unit, y * unit];
}

export function gameTextWorld(text: string, origin: Pt, heightMm = 8): Pt[][] {
  const unit = heightMm / CAP_HEIGHT;
  const lines: Pt[][] = [];
  let x = origin[0];
  let y = origin[1];
  for (const ch of normalizeGameText(text)) {
    if (ch === "\n") {
      x = origin[0];
      y += unit * LINE_HEIGHT;
      continue;
    }
    const glyph = GLYPHS[ch] ?? GLYPHS[" "];
    for (const stroke of glyph.strokes) {
      lines.push(stroke.map((point) => {
        const [sx, sy] = stylizePoint(point, unit);
        return [x + sx, y + sy] as Pt;
      }));
    }
    x += glyph.advance * WIDTH_SCALE * unit;
  }
  return lines;
}
